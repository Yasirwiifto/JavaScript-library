// equality operators

// strict Equality Operator (Always checks if they have the same Type and Value)
 console.log(1 === 1);

// comparing string with a number
 console.log("1" === 1);

// Lose Equality Operator (Always checks if the Values are equal)
console.log(1 == 1);
console.log("1" == 1);
console.log(true == 1);
